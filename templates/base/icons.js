/**
 * Auto-detect appropriate icon for a service name
 */
export function detectIcon(serviceName) {
  if (!serviceName) return 'star';

  const nameLower = serviceName.toLowerCase();

  for (const [keyword, icon] of Object.entries(ICON_MAPPINGS)) {
    if (nameLower.includes(keyword)) {
      return icon;
    }
  }

  return 'star'; // Default fallback
}

